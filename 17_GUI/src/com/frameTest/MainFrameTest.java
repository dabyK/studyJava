package com.frameTest;

import javax.swing.JFrame;

public class MainFrameTest {
	
	public static void main(String[] args) {
		
		//프레임 -> 윈도우 창을 의미한다. 창 한개!!
		JFrame f=new JFrame();//프레임생성!
		f.setBounds(100,100,300,400);//프레임의 위치,크기 설정
		
		f.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);//닫힘 버튼 작동하게 설정
		
		f.setVisible(true);//생성된 프레임을 보이게 하는 속성
		
		
		
		
		
		
		
		
		
		
		
	}
	
	

}
